const formatReservationsForWeb = async (reservationList) => {
    //utilities
    const months = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December']
    //utilities
    // const incommigReservations = (reservationList) ? reservationList : reservations;
    const incommigReservations = reservationList;
    // console.log(incommigReservations)
    const formattedReservations = [];
    for (let index = 0; index < incommigReservations.length; index++) {
        try {
            const incommigReservation = incommigReservations[index];
            const date = new Date(Date.parse(incommigReservation.date));
            //console.log(incommigReservation)
            formattedReservations[index] = {
                id: index + 1,
                title: `${months[date.getMonth()]} ${date.getDate()}, ${date.getFullYear()}${(new Date().getDate() == date.getDate()) ? " - Today" : ""}`,
                day: date.getDate(),
                month: date.getMonth() + 1,
                year: date.getFullYear(),
                card: [
                    {
                        id: incommigReservation.id,
                        name: "Seat Reservation",
                        meetingRoom: `${incommigReservation.Seat.Room.name} - ${incommigReservation.Seat.Room.Floor.name} Floor`,
                        time: incommigReservation.timeBlock,
                        seat: `Seat No. ${incommigReservation.Seat.number}`
                    }
                ]
            }
        } catch (err) { console.log(err); continue; }
    }
    return formattedReservations;
}



